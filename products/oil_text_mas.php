<p style="text-align: justify;">
	 Automatic transmission fluid (ATF) is one of the specific products of autochemistry as automatic transmissions include several completely different units - the torque converter, planetary gears, a complex system of hydraulic control. For these reasons, ATF range of functions is very large: it lubricates, removes excess heat from the components, serves as the working fluid for the automation of automatic transmissions, protects against corrosion and wear.
